class movie {
  constructor(title, director, genres, actors, runtime, release, summary, ageRating, price) {
    this.title = title;
    this.director = director;
    this.genres = genres;
    this.actors = actors;
    this.runtime = runtime;
    this.release = release;
    this.summary = summary;
    this.ageRating = ageRating;
    this.price = price;
  }
}